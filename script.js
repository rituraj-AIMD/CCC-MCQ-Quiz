function generateQuiz() {
    let quizForm = document.getElementById("quiz-form");
    quizForm.innerHTML = "";

    for (let i = 0; i < questions.length; i++) {
        let questionHTML = `
            <h2>Question ${i + 1}: ${questions[i].question}</h2>
            <ul>
                <li><input type="radio" name="question${i}" value="0">${questions[i].options[0]}</li>
                <li><input type="radio" name="question${i}" value="1">${questions[i].options[1]}</li>
                <li><input type="radio" name="question${i}" value="2">${questions[i].options[2]}</li>
                <li><input type="radio" name="question${i}" value="3">${questions[i].options[3]}</li>
            </ul>
        `;
        quizForm.innerHTML += questionHTML;
    }
}

document.getElementById("submit-btn").addEventListener("click", () => {
    let questionCount = 0;
    let wrongAnswers = 0;
    let correctAnswers = [];
    let incorrectQuestions = [];

    for (let i = 0; i < questions.length; i++) {
        let userAnswer = getSelectedAnswer(i);
        userAnswers.push(userAnswer);
        correctAnswers.push(questions[i].options[questions[i].correctAnswer]);

        if (userAnswer !== questions[i].correctAnswer) {
            wrongAnswers++;
            incorrectQuestions.push({
                question: questions[i].question,
                correctAnswer: questions[i].options[questions[i].correctAnswer]
            });
        }

        questionCount++;
    }

    displayResult(questionCount, wrongAnswers, correctAnswers, incorrectQuestions);
});

function getSelectedAnswer(questionIndex) {
    let radios = document.getElementsByName(`question${questionIndex}`);
    for (let i = 0; i < radios.length; i++) {
        if (radios[i].checked) {
            return i;
        }
    }
    return null;
}

function displayResult(questionCount, wrongAnswers, correctAnswers, incorrectQuestions) {
    let resultHTML = `
        <h2>Result</h2>
        <p>You scored ${questionCount - wrongAnswers} out of ${questionCount}.</p>
    `;
    document.getElementById("result").innerHTML = resultHTML;

    let attemptSummaryHTML = `
        <h2>Attempt Summary</h2>
        <p>You answered ${questionCount - wrongAnswers} questions correctly.</p>
        <p>You answered ${wrongAnswers} questions incorrectly.</p>
    `;
    document.getElementById("attempt-summary").innerHTML = attemptSummaryHTML;

    let incorrectAnswersHTML = `
        <h2>Incorrect Answers</h2>
    `;
    for (let i = 0; i < incorrectQuestions.length; i++) {
        incorrectAnswersHTML += `
            <p>Question: ${incorrectQuestions[i].question}</p>
            <p>Correct answer: ${incorrectQuestions[i].correctAnswer}</p>
        `;
    }
    document.getElementById("incorrect-answers").innerHTML = incorrectAnswersHTML;
}